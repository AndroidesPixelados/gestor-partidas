public class PersonajePrueba {
	
	private String nombre;
	private String tipo;
	/**
	 * Obtiene el nombre.
	 * @return nombre.
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * Establece el  nombre
	 * @param nombre establece nombre a nombre
	 * @return el objeto
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * Obtiene el tipo.
	 * @return tipo.
	 */
	public String getTipo() {
		return tipo;
	}
	/**
	 * Establece el  tipo
	 * @param tipo establece tipo a tipo
	 * @return el objeto
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	

}
